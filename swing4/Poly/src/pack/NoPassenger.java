package pack;

public class NoPassenger {

	String name;
	String ageandgender;
	String _class;
	String seatno;
	int cost;
	BookingDetail detail;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAgeandgender() {
		return ageandgender;
	}
	public void setAgeandgender(String ageandgender) {
		this.ageandgender = ageandgender;
	}
	public String get_class() {
		return _class;
	}
	public void set_class(String _class) {
		this._class = _class;
	}
	public String getSeatno() {
		return seatno;
	}
	public void setSeatno(String seatno) {
		this.seatno = seatno;
	}
	public int getCost() {
		return cost;
	}
	public void setCost(int cost) {
		this.cost = cost;
	}
	public BookingDetail getDetail() {
		return detail;
	}
	public void setDetail(BookingDetail detail) {
		this.detail = detail;
	}
	
}
